using System;
namespace UnityEngine.UIElements;

/// <summary>
/// Provides a way to register multiple copies of the same callback group on a given element, with each registration
/// allowing a distinct `TArg` value to be provided.
///
/// For a given element, the factory will attempt to register each of its internal copies of the callback group
/// until it finds one that's not already registered on the element. If none is found, the factory creates a new
/// internal copy of the callback group and keeps it for later use if other elements also need multiple registrations.
/// The chosen callback group copy is then registered and its associated EventArg is set to the desired `TArg` value.
///
/// Each registration returns a <see cref="Group"/> that can then be unregistered
/// individually.
/// </summary>
/// <typeparam name="TArg">The type of value that can be set on each callback group instantiated using this factory.</typeparam>
internal class EventCallbackGroupFactory<TArg>
{
    private readonly Func<EventArg<TArg>, EventCallbackGroup> m_CreateGroup;
    /// <summary>
    /// Creates a factory that produces instances of <see cref="EventCallbackGroup"/> when needed, using the provided
    /// @@createGroup@@ method. Callback groups produced by this factory are provided with a unique
    /// <see cref="EventArg{TArg}"/> argument identifier that can be used to set a distinct value for callbacks within
    /// that group when registering it on an element.
    /// </summary>
    /// <param name="createGroup"></param>
    /// <seealso cref="Register"/>
    public EventCallbackGroupFactory(Func<EventArg<TArg>, EventCallbackGroup> createGroup)
    {
        m_CreateGroup = createGroup;
    }

    /// <summary>
    /// An object that holds the specific <see cref="EventCallbackGroup"/> information and can be unregistered.
    /// </summary>
    /// <seealso cref="Unregister"/>
    public class Group
    {
        internal EventCallbackGroupFactory<TArg> factory;
        internal EventArg<TArg> arg;
        internal EventCallbackGroup group;
        internal int refCount;
        internal Group prev, next;

        public void Unregister(VisualElement ve)
        {
            factory.Unregister(this, ve);
        }
    }

    private Group m_First;
    private Group GetNonRegisteredGroupForElement(VisualElement ve)
    {
        Group prev = null;
        for (var group = m_First; group != null; prev = group, group = group.next)
        {
            // Although this is a linear search inside a loop, it should be extremely rare to have more than 2 or 3
            // same-group instances on a single element. Until this is flagged as a problem, we can assume this is OK.
            if (!group.group.IsRegistered(ve))
            {
                return group;
            }
        }

        var newArg = EventArg.Create<TArg>();
        var newGroup = new Group { factory = this, arg = newArg, group = m_CreateGroup(newArg), prev = prev };

        if (prev != null)
            prev.next = newGroup;
        else
            m_First = newGroup;

        return newGroup;
    }

    /// <summary>
    /// Registers a copy of the <see cref="EventCallbackGroup"/> generated by this factory and registers the provided
    /// @@argValue@@ for the corresponding <see cref="EventArg{TArg}"/>.
    /// </summary>
    /// <param name="ve">The element on which the group and arg value are registered</param>
    /// <param name="argValue">The value registered for the group's associated argument identifier</param>
    /// <returns>An object that can be unregistered later.</returns>
    public Group Register(VisualElement ve, TArg argValue)
    {
        var group = GetNonRegisteredGroupForElement(ve);
        group.arg.Register(ve, argValue);
        group.group.Register(ve);
        group.refCount++;
        return group;
    }

    private void Unregister(Group group, VisualElement ve)
    {
        if (group == null)
            return;

        if (!group.arg.Unregister(ve))
            return;
        group.group.Unregister(ve);

        if (--group.refCount > 0)
            return;

        if (group.prev != null)
            group.prev.next = group.next;
        else
            m_First = group.next;

        if (group.next != null)
            group.next.prev = group.prev;
    }
}
